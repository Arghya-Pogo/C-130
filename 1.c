// WAP IN C THAT TAKE 2 NUMBER AND PERFORM THE ADD, SUBSTRACTION, MULTIPLICATION, DIVISION

#include <stdio.h>

int main() {
    int a, b;

    printf("Enter the two numbers:    ");
    scanf("%d %d", &a, &b);

    printf("Sum: %d \n", a+b);
    printf("Sum: %d \n", a-b);
    printf("Sum: %d \n", a*b);
    printf("Sum: %d \n", a/b);

    return 0;
}